/*
 주제 : 화면을 확대/축소하는 ZOOM 버튼 만들기
 ----------------------------------------------------------

 [+] 버튼 : 화면을 크게 만든다. (확대)
 [-] 버튼 : 화면을 작게 만든다. (축소)
 [100] 버튼 : 화면 크기를 원래 100% 상태로 되돌린다.

 ※ 화면 크기를 직접 바꾸려면 CSS에서 지원하는
    'zoom' 이나 'transform: scale()' 이라는 기능을 사용해야 한다.


 ----------------------------------------------------------
 [1] zoom 속성 (간단하게 화면 비율을 바꾸는 방법)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("zoom", "확대비율%");
 - 예시   : $("body").css("zoom", "200%");
            → body(전체 화면)을 200% 크기로 확대한다.

 - 특징 : 사용하기 매우 쉽다.
 - 단점 : Firefox(파이어폭스) 브라우저에서는 동작하지 않는다.
          (Chrome, Edge, Safari 등 대부분 브라우저에서는 잘 됨)


 ----------------------------------------------------------
 [2] transform: scale() 속성 (모든 브라우저에서 잘 동작하는 편)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("transform", "scale(비율)");
 - 예시   : $("body").css("transform", "scale(2)");
            → body(전체 화면)을 2배로 확대한다.

 - 특징 : Firefox 포함 거의 모든 브라우저에서 동작한다.
 - 주의 : 확대 기준점이 화면 중앙이라서,
          화면이 중심을 기준으로 퍼지듯이 확대된다.


 ----------------------------------------------------------
 [3] transform-origin 속성 (확대 기준점을 바꾸는 기능)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("transform-origin", "x값 y값");
 - 예시   : $("body").css("transform-origin", "0 0");
            → 화면의 왼쪽 위(0,0)에서부터 확대되도록 기준점을 변경한다.

 - 설명 : 기본적으로 scale()은 가운데를 기준으로 확대되는데,
          기준점을 바꾸면 왼쪽 위 또는 원하는 위치에서 확대되게 만들 수 있다.


 ----------------------------------------------------------
 정리
 ----------------------------------------------------------
 [+] 클릭 → 확대 (비율 증가)
 [-] 클릭 → 축소 (비율 감소)
 [100] 클릭 → 원래 크기(100%)로 되돌림

 zoom은 간단하지만 파이어폭스에서 안 되고,
 transform: scale()은 브라우저 호환성이 좋아서 많이 사용한다.
 */

  
/* zoom 버튼 */
// 기준값 저장
let base = 100;

// body 요소 저장
let mybody = $("body");

// id가 zoom인 부모 요소를 먼저 선택해서 후손 선택자를 이용해서 후손 a 요소들을 배열에 담아 선택
// click 이벤트 등록
$("#zoom a").on("click", function(){
	// click 이벤트가 발생한 요소의 index 값 찾기
	let zNum = $("#zoom a").index(this);
	
	// index 값이 0이라면
	if(zNum === 0){
		base += 10; // 확대값을 10% 누적해서 증가시킴
		// index 값이 1이라면	
	}else if(zNum === 1){
		base = 100;	// 기본값 100으로 설정
		// index 값이 2라면
	}else{
		base -= 10; // 축소값을 10% 누적해서 감소시킴
	}
	
	mybody
	// IE 8버전 이하에서 정상적으로 작동하도록 설정
	.css("overflow-x", "auto")	
	// 확대 기준점을 x=0, y=0으로 설정	
	.css("transform-origin", "0 0")
	// body 태그에 포함된 자식 요소들도 base/100만큼 확대/축소
	.css("transform", "scale("+ base/100 +")") 
	// body 태그 전체 화면 영역의 모든 태그가 base 값 %만큼 확대/축소
	.css("zoom", base + "%");
	;
	
	// a 태그의 기본 이벤트 차단
	return false;
})



