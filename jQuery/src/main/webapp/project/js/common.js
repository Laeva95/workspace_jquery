	// 1초마다 시간 갱신
	window.setInterval(function(){
		
		// 1초마다 1000 누적
		elapsedTime += 1000;
		
		// 기존 시간에 1000 ms를 누적해서 갱신
		date.setTime(initialTime + elapsedTime);

		// 갱신된 시간을 가져와서 각 요소에 text 입력
		dateEl.eq(0).text(date.getFullYear());	// 연도
		dateEl.eq(1).text(date.getMonth() + 1);	// 월( + 1을 반드시 입력해야함)
		dateEl.eq(2).text(date.getDate());		// 일
		dateEl.eq(3).text(date.getHours());		// 시
		dateEl.eq(4).text(date.getMinutes());	// 분
		dateEl.eq(5).text(date.getSeconds());	// 초
	}, 1000);


